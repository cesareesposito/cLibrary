namespace cLibrary.Enums
{
    public enum cBtnState
    {
        /// <summary>
        /// The button is disabled.
        /// </summary>		
        Inactive = 0,
        /// <summary>
        /// The button is in it normal unpressed state
        /// </summary>
        Normal = 1,
        /// <summary>
        /// The location of the mouse is over the button
        /// </summary>
        MouseOver = 2,
        /// <summary>
        /// The button is currently being pressed
        /// </summary>
        Pushed = 3,
    }
}
